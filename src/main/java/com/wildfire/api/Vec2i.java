package com.wildfire.api;

/**
 * Basic immutable version of a {@link org.joml.Vector2i Vector2i}
 */
public record Vec2i(int x, int y) {
	public Vec2i withX(int x) {
		return new Vec2i(x, this.y);
	}
}
